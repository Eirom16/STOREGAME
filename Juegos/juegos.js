const filasPorPagina = 20;  // cuántas filas mostrar por página
const juegosPorFila = 4;    // columnas por fila
const juegosPorPagina = filasPorPagina * juegosPorFila;

const contentMain = document.querySelector('.content-main');

let paginaActual = 1;
const juegosIds = Object.keys(juegos);
const totalPaginas = Math.ceil(juegosIds.length / juegosPorPagina);

function mostrarPagina(pagina) {
  // Limpiar el contenido actual
  contentMain.innerHTML = '';

  // Calcular el rango de juegos que se mostrarán en esta página
  const inicio = (pagina - 1) * juegosPorPagina;
  const fin = inicio + juegosPorPagina;
  const juegosAMostrar = juegosIds.slice(inicio, fin);

  // Crear filas y juegos
  for (let i = 0; i < juegosAMostrar.length; i += juegosPorFila) {
    const fila = document.createElement('div');
    fila.className = 'filas';

    const juegosEnFila = juegosAMostrar.slice(i, i + juegosPorFila);
    juegosEnFila.forEach(id => {
      const juego = juegos[id];

      const divJuego = document.createElement('div');
      divJuego.className = 'game-download';

      const enlace = document.createElement('a');
      enlace.href = `Informacion/HTML/informacion.html?id=${id}`;

      const imagen = document.createElement('img');
      imagen.src = juego.imagen;
      imagen.className = 'portada';
      imagen.alt = juego.titulo;

      const titulo = document.createElement('h3');
      titulo.className = 'titulo-game';
      titulo.textContent = juego.titulo;

      enlace.appendChild(imagen);
      enlace.appendChild(titulo);
      divJuego.appendChild(enlace);
      fila.appendChild(divJuego);
    });

    contentMain.appendChild(fila);
  }

  // Mostrar controles de paginación al final del contenedor
  mostrarControles();
}

function mostrarControles() {
  // Eliminar controles antiguos si existen
  const antiguosControles = document.querySelector('.paginacion');
  if (antiguosControles) antiguosControles.remove();

  // Crear contenedor para botones
  const controles = document.createElement('div');
  controles.className = 'paginacion';
  controles.style.textAlign = 'center';
  controles.style.marginTop = '20px';

  // Botón "Anterior"
  const btnAnterior = document.createElement('button');
  btnAnterior.textContent = '← Anterior';
  btnAnterior.className = 'btn-paginacion anterior';
  btnAnterior.disabled = (paginaActual === 1);
  btnAnterior.onclick = () => {
    if (paginaActual > 1) {
      paginaActual--;
      mostrarPagina(paginaActual);
      window.scrollTo({top: 0, behavior: 'smooth'});
    }
  };

  // Texto central
  const textoPagina = document.createElement('span');
  textoPagina.className = 'texto-pagina';
  textoPagina.textContent = ` Página ${paginaActual} de ${totalPaginas} `;

  // Botón "Siguiente"
  const btnSiguiente = document.createElement('button');
  btnSiguiente.textContent = 'Siguiente →';
  btnSiguiente.className = 'btn-paginacion siguiente';
  btnSiguiente.disabled = (paginaActual === totalPaginas);
  btnSiguiente.onclick = () => {
    if (paginaActual < totalPaginas) {
      paginaActual++;
      mostrarPagina(paginaActual);
      window.scrollTo({top: 0, behavior: 'smooth'});
    }
  };

  // Añadir elementos al contenedor
  controles.appendChild(btnAnterior);
  controles.appendChild(textoPagina);
  controles.appendChild(btnSiguiente);

  // Insertar los controles al final del contentMain
  contentMain.appendChild(controles);
}

// Mostrar la primera página al cargar
mostrarPagina(paginaActual);
